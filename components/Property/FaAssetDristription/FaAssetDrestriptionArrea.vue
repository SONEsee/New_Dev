  const data =  eodStore.response_data_eod;
  if (Array.isArray(data)) {
    return data;
  }
  if (data && typeof data === "object") {
    return [data];
  }
  return [];
})

const assetdata = computed(()=>{
  const data =  assetStores.response_fa_asset_list;
  if (Array.isArray(data)) {
    return data;
  }
  if (data && typeof data === "object") {
    return [data];
  }
  return [];
})

const res = computed(() => {
  const data = mainStore.response_data_get_overdue?.data.overdue_items;
  if (Array.isArray(data)) {
    return data;
  }
  if (data && typeof data === "object") {
    return [data];
  }
  return [];
});

// เปเบเบตเปเบก computed เบชเบณเบฅเบฑเบ EOD target date
const eodTargetDate = computed(() => {
  const eodData = eod.value[0]; // เปเบญเบปเบฒ EOD เบเบณเบญเบดเบ
  if (eodData && eodData.prev_working_day) {
    return dayjs(eodData.prev_working_day).format("YYYY-MM-DD");
  }
  // เบเปเบฒเบเปเปเบกเบต EOD เบซเบผเบท prev_working_day เปเบซเปเปเบเปเบงเบฑเบเบเบฐเบเบธเบเบฑเบ
  return dayjs().tz("Asia/Bangkok").format("YYYY-MM-DD");
});

// เบชเปเบฒเบ computed เบชเบณเบฅเบฑเบเปเบกเบฑเบเบเปเปเบกเบนเบ
// เบเบฑเบเบเบธเบเบเบฒเบเบเบดเบเปเบฅเปเปเบ computed mappedData
const mappedData = computed(() => {
  return res.value.map(overdueItem => {
    // เบซเบฒ asset เบเบตเปเบเบปเบเบเบฑเบเปเบเบเปเบเป asset_id
    const matchedAsset = assetdata.value.find(asset => 
      asset.asset_list_id === overdueItem.asset_id
    );
    
    if (matchedAsset) {
      // เบเบดเบเปเบฅเปเบเปเบฒเบเบตเปเบเปเบญเบเบเบฒเบ: เบเบฒเบเบเบดเบเปเบฅเปเปเปเปเบเบฒเบกเปเบเบทเปเบญเบเปเบ
      const dailyDepreciation = parseFloat(matchedAsset.asset_value_remainMonth) / 30;
      let calculatedAmount = 0;
      
      // เปเบเบฑเบเบเปเบฒ C_dpac
      const cDpacValue = parseInt(matchedAsset.C_dpac) || 0;
      const overdueMonthsFloor = Math.floor(overdueItem.overdue_months); // เบเปเปเบเบฑเบเบซเบผเบฑเบเบเบธเบ
      const assetValueRemainMonth = parseFloat(matchedAsset.asset_value_remainMonth);
      const assetValueRemainBegin = parseFloat(matchedAsset.asset_value_remainBegin);
      const accuDpcaValueTotal = parseFloat(matchedAsset.accu_dpca_value_total);
      const totalMonths = overdueItem.total_months;
      
      if (cDpacValue === 0) {
        // C_dpac เบเบฑเบเบเปเปเบกเบตเบเปเบฒ (เปเบเบปเปเบฒเบเบฑเบ 0)
        if (overdueMonthsFloor >= totalMonths) {
          // เบเปเบฒ overdue_months >= total_months เปเบซเปเปเบเป accu_dpca_value_total
          calculatedAmount = accuDpcaValueTotal;
        } else {
          // เบเบฒเบเบเบดเบเปเบฅเปเบเบปเบเบเบฐเบเบด: (overdue_months - 1) * asset_value_remainMonth + asset_value_remainBegin
          const adjustedMonths = Math.max(0, overdueMonthsFloor - 1);
          calculatedAmount = (adjustedMonths * assetValueRemainMonth) + assetValueRemainBegin;
        }
      } else {
        // C_dpac เบกเบตเบเปเบฒเปเบฅเปเบง (เบเปเปเปเบเบปเปเบฒเบเบฑเบ 0)
        if (overdueMonthsFloor >= totalMonths) {
          // เบเปเบฒ overdue_months >= total_months เปเบซเปเปเบเป accu_dpca_value_total
          calculatedAmount = accuDpcaValueTotal;
        } else {
          // เบเบฒเบเบเบดเบเปเบฅเป: overdue_months * asset_value_remainMonth
          calculatedAmount = overdueMonthsFloor * assetValueRemainMonth;
        }
      }
      
      return {
        ...overdueItem,
        // เปเบเบตเปเบกเบเปเปเบกเบนเบเบเบฒเบ asset
        matched_asset: matchedAsset,
        daily_depreciation: dailyDepreciation,
        calculated_overdue_amount: calculatedAmount,
        asset_value_remainMonth: matchedAsset.asset_value_remainMonth,
        // เปเบเบตเปเบกเบเปเปเบกเบนเบเบญเบทเปเบเป เบเบตเปเบญเบฒเบเบเบฐเปเบเป
        asset_full_name: matchedAsset.asset_id_detail?.asset_name_la || overdueItem.asset_name,
        location_name: matchedAsset.location_detail?.location_name_la,
        supplier_name: matchedAsset.supplier_detail?.supplier_name,
        // เปเบเบตเปเบกเบเปเปเบกเบนเบเบชเบณเบฅเบฑเบ debug
        debug_info: {
          c_dpac: cDpacValue,
          overdue_months_floor: overdueMonthsFloor,
          total_months: totalMonths,
          asset_value_remain_month: assetValueRemainMonth,
          asset_value_remain_begin: assetValueRemainBegin,
          accu_dpca_value_total: accuDpcaValueTotal,
          calculation_method: cDpacValue === 0 ? 'C_dpac_zero' : 'C_dpac_has_value',
          eod_target_date: eodTargetDate.value // เปเบเบตเปเบกเบงเบฑเบเบเบตเป EOD เบชเบณเบฅเบฑเบ debug
        }
      };
    }
    
    // เบเปเบฒเบเปเปเบเบปเบ asset เบเบตเปเบเบปเบเบเบฑเบ
    return {
      ...overdueItem,
      matched_asset: null,
      daily_depreciation: 0,
      calculated_overdue_amount: 0,
      asset_value_remainMonth: 0
    };
  });
});

